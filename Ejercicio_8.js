// Crear un vector de cinco elementos de cadenas de caracteres
var vectorOriginal = ["Elemento1", "Elemento2", "Elemento3", "Elemento4", "Elemento5"];

// Mostrar el vector original
console.log("Vector Original:", vectorOriginal);

// Copiar los elementos del vector en otro vector, pero en orden inverso
var vectorInvertido = vectorOriginal.slice().reverse();

// Mostrar el vector invertido
console.log("Vector Invertido:", vectorInvertido);

